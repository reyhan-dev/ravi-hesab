function createToast(message, type = "info") {
  const toast = document.createElement("div");
  toast.className = `relative p-4 text-secondary rounded-lg shadow-lg overflow-hidden ${getTypeClass(
    type
  )}`;

  toast.innerHTML = `
      <div class="flex items-center gap-3">
          <div class="flex-shrink-0">${getIcon(type)}</div>
          <div class="ml-3 flex-1">
              <p class="text-sm">${message}</p>
          </div>
      </div>
      <div class="absolute bottom-0 left-0 h-1 bg-black/10 toast-progress"></div>
  `;

  return toast;
}

function showToast(
  message,
  type = "info",
  duration = 3000
) {
  const container = document.getElementById(
    "toaster-container"
  );
  const toast = createToast(message, type);

  container.appendChild(toast);

  setTimeout(() => {
    toast.remove();
  }, duration);
}

function getTypeClass(type) {
  switch (type) {
    case "success":
    case "error":
    case "warning":
    case "info":
      return "bg-white shadow-lg border border-borderColor";
  }
}
